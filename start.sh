#!/bin/bash

# 가상환경 활성화
source venv/bin/activate

echo "=========================================="
echo "   자동매매 봇을 실행합니다..."
echo "   (종료하려면 Ctrl+C를 누르세요)"
echo "=========================================="

# 봇 실행 (내부 포트 8000 사용)
# 외부(인터넷) -> 도메인(Nginx, 80/443) -> 봇(8000)으로 연결됨
uvicorn app.main:app --host 127.0.0.1 --port 8000 --reload